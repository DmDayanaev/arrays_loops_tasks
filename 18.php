<?php
//18. Составьте массив дней недели. С помощью цикла foreach выведите все дни недели, выходные дни следует вывести жирным.
$arr = ["пон", "втор", "сред", "четв", "пятница", "суббота", "воскр"];
$weekend = ["суббота", "воскр"];
foreach ($arr as $v) {
    if (in_array($v, $weekend)) {
        echo "<b>".$v."</b> ";
    } else {echo $v." ";}
}